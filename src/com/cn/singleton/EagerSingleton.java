package com.cn.singleton;
/**
 * 
 * @author tianchengju
 *饿汉模式
 */
public class EagerSingleton {
	private static EagerSingleton instance = new EagerSingleton();
	
	/**
	 * 私有默认构造方法
	 */
	private EagerSingleton(){}
	/**
	 * 静态工厂方法
	 */
	public static EagerSingleton getInstance(){
		return instance;
	}
}
